    <?php
    $x = 0;
    $y = 0;

    // Pre-increment
    // Increments $x by one, then returns $x
    ++$x;
    // Post-increment
    // Devuelve $x, luego incrementa $x en uno
    $x++;

    // Pre-decrement
    // Disminuye $x en uno, luego devuelve $x
    --$y;
    // Post-decrement
    // Devuelve $x, luego disminuye $x en uno
    $y--;

    ?>
